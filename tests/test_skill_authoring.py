@pytest.mark.asyncio
async def test_deploy_saves_to_user_dir(authoring, mock_loader):
    """Deploy should call save_skill and update status."""
    draft = SkillDraft(name="my-skill", description="", content=VALID_SKILL_CONTENT)

    with patch("cognitex.agent.action_log.log_action", new_callable=AsyncMock):
        success = await authoring.deploy_skill(draft)

    assert success is True
    mock_loader.save_skill.assert_called_once_with("my-skill", VALID_SKILL_CONTENT)
    assert draft.status == "deployed"


@pytest.mark.asyncio
async def test_deploy_failure(authoring, mock_loader):
    """Deploy failure should return False and not update status."""
    mock_loader.save_skill = AsyncMock(return_value=False)

    draft = SkillDraft(name="fail", description="", content="content")
    success = await authoring.deploy_skill(draft)

    assert success is False
    assert draft.status == "draft"
