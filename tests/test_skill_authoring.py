def test_slugify_basic():
    assert _slugify("Email Task Extraction") == "email-task-extraction"


def test_slugify_special_chars():
    assert _slugify("My Skill! v2.0") == "my-skill-v20"


def test_slugify_truncates():
    result = _slugify("a" * 100)
    assert len(result) <= 50


def test_strip_code_fences():
    text = "```yaml\n---\nname: foo\n---\n```"
    assert _strip_code_fences(text) == "---\nname: foo\n---"


def test_strip_code_fences_no_fences():
    text = "plain text"
    assert _strip_code_fences(text) == "plain text"


