@pytest.mark.asyncio
async def test_create_from_description(authoring, mock_llm):
    """Should generate a valid SkillDraft with parseable frontmatter."""
    draft = await authoring.create_from_description(
        description="Extract tasks from Slack messages",
    )

    assert isinstance(draft, SkillDraft)
    assert draft.name == "test-generated"  # from frontmatter
    assert draft.description == "A test skill generated by AI."
    assert "## Rules" in draft.content
    assert draft.version == 1
    assert draft.status == "draft"

    # LLM was called
    mock_llm.complete.assert_called_once()


@pytest.mark.asyncio
async def test_create_with_explicit_name(authoring, mock_llm):
    """Explicit name is passed to the prompt template."""
    draft = await authoring.create_from_description(
        description="Test",
        name="my-custom-skill",
    )

    # The prompt should contain the explicit name
    call_args = mock_llm.complete.call_args
    assert "my-custom-skill" in call_args.kwargs.get("prompt", call_args[1].get("prompt", ""))


@pytest.mark.asyncio
async def test_create_with_examples(authoring, mock_llm):
    """Examples should be included in the LLM prompt."""
    draft = await authoring.create_from_description(
        description="Test skill",
        examples=["Input: hello -> Output: hi", "Input: bye -> Output: goodbye"],
    )

    call_args = mock_llm.complete.call_args
    prompt = call_args.kwargs.get("prompt", call_args[1].get("prompt", ""))
    assert "hello" in prompt
    assert "goodbye" in prompt


