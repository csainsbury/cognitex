@pytest.mark.asyncio
async def test_test_skill_returns_results(authoring, mock_llm):
    """Test skill should return results with success=True for valid output."""
    mock_llm.complete = AsyncMock(return_value="Extracted task: Review PR")

    draft = SkillDraft(name="test", description="", content=VALID_SKILL_CONTENT)
