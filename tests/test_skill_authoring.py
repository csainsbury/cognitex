REFINED_CONTENT = """\
---
name: test-generated
description: A refined test skill.
version: 1.0.1
metadata:
  cognitex: {}
---

# Test Generated (Refined)

## Purpose
Refined purpose.

## Rules
1. Updated rule
"""


@pytest.mark.asyncio
async def test_refine_increments_version(authoring, mock_llm):
    """Refining should increment the version number."""
    mock_llm.complete = AsyncMock(return_value=REFINED_CONTENT)

    draft = SkillDraft(
        name="test-generated",
        description="original",
        content=VALID_SKILL_CONTENT,
        version=1,
    )
    refined = await authoring.refine_draft(draft, "Make it shorter")

    assert refined.version == 2
    assert "Updated rule" in refined.content


@pytest.mark.asyncio
async def test_refine_appends_feedback(authoring, mock_llm):
    """Feedback history should grow with each refinement."""
    mock_llm.complete = AsyncMock(return_value=REFINED_CONTENT)

    draft = SkillDraft(
        name="test",
        description="",
        content=VALID_SKILL_CONTENT,
        version=1,
        feedback_history=["first feedback"],
    )
    refined = await authoring.refine_draft(draft, "second feedback")

    assert len(refined.feedback_history) == 2
    assert refined.feedback_history[-1] == "second feedback"


